# Tipos de datos en Python

# string (cadena de texto)
str = "string"
print(type(str))

# int (numeros enteros)
int = 123
print(type(int))

# float (numeros con decimales)
float = 3.14
print(type(float))

# boolean (verdadero o falso)
bool = True
bool2 = False
print(type(bool))
print(type(bool2))

# complex (numeros complejos)
complex = 1 + 2j
print(type(complex))

