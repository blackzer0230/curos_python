      ### OPERADORES  ###

# operadores aritmeticos basicos

# suma 
print(2 + 3)

# resta
print(4 - 2)

# multiplicacion
print(2 * 5)

# division
print(10 / 4)

# modulo
print(10 % 2)

# numero en que se dividio
print(10 // 2)

# potencia
print(2 ** 2)

# operadores con string
print('hola ' + 'como estas? ' + 'adios')
print("hola " * 10)


### operadores comparativos ### 

# mayoe que 
print(4 > 5)

# menor que 
print(4 < 5)

# mayor o igual que 
print(4 >= 5)

# menor o igual que
print(4 <= 5)

# igual que 
print(4 == 6)

# diferente que
print(4 != 6)

 ### operadores logicos ###

# todo esto es logica boleana si todas se cumplen
print( 3 > 4 and "hola" > "world")

# solo si una de ella se cumple
print( 3 > 4 or "hola" > "world")

# para cambiar el valor
print(not(3 > 4) )
